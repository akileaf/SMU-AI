export function generateChartPoints(basePrice: number, pointsCount = 30, volatility = 0.015): ChartPoint[] {
  const points: ChartPoint[] = [];
  let currentPrice = basePrice * 0.95;
  const now = new Date();

  for (let i = 0; i < pointsCount; i++) {
    const time = new Date(now.getTime() - (pointsCount - i) * 15 * 60 * 1000).toLocaleTimeString([], {
      hour: '2-digit',
      minute: '2-digit',
    });
    const change = (Math.random() - 0.48) * volatility * currentPrice;
    const open = currentPrice;
    const close = Math.max(1, currentPrice + change);
    const high = Math.max(open, close) + Math.random() * volatility * currentPrice * 0.5;
    const low = Math.min(open, close) - Math.random() * volatility * currentPrice * 0.5;
    const volume = Math.floor(Math.random() * 500000 + 100000);

    points.push({
      time,
      price: Number(close.toFixed(2)),
      open: Number(open.toFixed(2)),
      high: Number(high.toFixed(2)),
      low: Number(low.toFixed(2)),
      close: Number(close.toFixed(2)),
      volume,
    });

    currentPrice = close;
  }

  // Ensure last point matches current market price
  if (points.length > 0) {
    points[points.length - 1].close = basePrice;
    points[points.length - 1].price = basePrice;
  }

  return points;
}

